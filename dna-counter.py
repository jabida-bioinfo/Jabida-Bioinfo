# Counts A, T, G, C in DNA sequence

dna = "AGCTTTTCATTCTGACTGCAACGGGCAATATGTCTCTGTGTGGATTAAAAAAAGAGTGTCTGATAGCAGC"

print("A →", dna.count('A'))
print("C →", dna.count('C'))
print("G →", dna.count('G'))
print("T →", dna.count('T'))
print("Length →", len(dna))
